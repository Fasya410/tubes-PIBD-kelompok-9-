CREATE DATABASE if NOT EXISTS aplikasikosan;
USE aplikasikosan;

CREATE TABLE Pengguna (
    ID_User INT AUTO_INCREMENT PRIMARY KEY,
    Nama VARCHAR(100),
    Email VARCHAR(100) UNIQUE,
    No_Telepon VARCHAR(20),
    Kata_Sandi VARCHAR(100),
    Tipe_Pengguna ENUM('Penyewa', 'Pemilik') NOT NULL
);

CREATE TABLE Penyewa (
    ID_Penyewa INT PRIMARY KEY,
    FOREIGN KEY (ID_Penyewa) REFERENCES Pengguna(ID_User)
);

CREATE TABLE Pemilik (
    ID_Pemilik INT PRIMARY KEY,
    FOREIGN KEY (ID_Pemilik) REFERENCES Pengguna(ID_User)
);

CREATE TABLE Properti (
    ID_Properti INT AUTO_INCREMENT PRIMARY KEY,
    ID_Pemilik INT,
    Nama_Properti VARCHAR(100),
    Alamat TEXT,
    Harga_Sewa DECIMAL(12,2),
    Fasilitas TEXT,
    Tipe_Properti ENUM('Kosan', 'Kontrakan'),
    Status_Ketersediaan ENUM('Tersedia', 'Tidak Tersedia'),
    FOREIGN KEY (ID_Pemilik) REFERENCES Pemilik(ID_Pemilik)
);

CREATE TABLE Kamar_Unit (
    ID_KamarUnit INT AUTO_INCREMENT PRIMARY KEY,
    ID_Properti INT,
    Harga_Sewa DECIMAL(12,2),
    Status_Ketersediaan ENUM('Tersedia', 'Tidak Tersedia'),
    FOREIGN KEY (ID_Properti) REFERENCES Properti(ID_Properti)
);

CREATE TABLE Fasilitas (
    ID_Fasilitas INT AUTO_INCREMENT PRIMARY KEY,
    Nama_Fasilitas VARCHAR(100)
);

CREATE TABLE Property_Fasilitas (
    ID_Property_Fasilitas INT AUTO_INCREMENT PRIMARY KEY,
    ID_KamarUnit INT,
    ID_Fasilitas INT,
    FOREIGN KEY (ID_KamarUnit) REFERENCES Kamar_Unit(ID_KamarUnit),
    FOREIGN KEY (ID_Fasilitas) REFERENCES Fasilitas(ID_Fasilitas)
);

CREATE TABLE Penyewaan (
    ID_Penyewaan INT AUTO_INCREMENT PRIMARY KEY,
    ID_Penyewa INT,
    ID_KamarUnit INT,
    Tanggal_Mulai_Sewa DATE,
    Tanggal_Selesai_Sewa DATE,
    Status_Sewa ENUM('Aktif', 'Selesai'),
    FOREIGN KEY (ID_Penyewa) REFERENCES Penyewa(ID_Penyewa),
    FOREIGN KEY (ID_KamarUnit) REFERENCES Kamar_Unit(ID_KamarUnit)
);

CREATE TABLE Pembayaran (
    ID_Pembayaran INT AUTO_INCREMENT PRIMARY KEY,
    ID_Penyewaan INT,
    Nominal_Pembayaran DECIMAL(12,2),
    Metode_Pembayaran VARCHAR(50),
    Status_Pembayaran ENUM('Belum Dibayar', 'Lunas'),
    FOREIGN KEY (ID_Penyewaan) REFERENCES Penyewaan(ID_Penyewaan)
);

CREATE TABLE Ulasan (
    ID_Ulasan INT AUTO_INCREMENT PRIMARY KEY,
    ID_Pembayaran INT,
    Rating INT,
    Kritik TEXT,
    Tanggal_Ulasan DATE,
    FOREIGN KEY (ID_Pembayaran) REFERENCES Pembayaran(ID_Pembayaran)
);

